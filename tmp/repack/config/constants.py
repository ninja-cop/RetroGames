BOARD_Y_MARGIN = 60

def get_board_positions():
    """ボード上の12位置の座標を計算して返す"""
    return [
        (BOARD_X_START + (i % 6) * BOARD_X_MARGIN, 
         BOARD_Y_START + (i // 6) * BOARD_Y_MARGIN) 
        for i in range(12)
    ]

